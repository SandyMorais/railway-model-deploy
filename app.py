app = Flask(__name__)

# @app.route('/') defines the route for the homepage
@app.route('/predict', methods=['POST'])

### The predict() function is responsible for generating the response
# that the user sees when accessing the '/predict' URL
def predict():
    try:
        obs_dict = request.get_json()
    except Exception:
        return jsonify({"error": "Invalid JSON"}), 400

    ### Run validation + prediction
    response = attempt_predict(obs_dict)

    if "error" in response:
        return jsonify(response), 400
    
    obs_id = response['observation_id']
    proba = response['probability']

    ### Save to database
    try:
        with DB.atomic():
            p = Prediction(
                observation_id = obs_id,
                proba = proba,
                observation = request.data
            )
            p.save()
    except IntegrityError:
        response["error"] = f'Observation id {obs_id} already exists'

    return jsonify(response)


@app.route('/update', methods=['POST'])
def update():
    try:
        obs = request.get_json()
    except Exception:
        return jsonify({'error': 'Invalid JSON'}), 400
    
    try:
        p = Prediction.get(Prediction.observation_id == obs['id'])
        p.true_class = obs['true_class']
        p.save()
        return jsonify(model_to_dict(p))
    
    except Prediction.DoesNotExist:
        return jsonify({'error':  f'Obs id {obs["id"]} does not exist'})
